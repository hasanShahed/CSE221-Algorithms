inp = open('/content/input2.txt','r')
out = open('/content/output2.txt','w')
read = inp.readline()
read1 = list(map(int,inp.readline().split(' ')))


def bubbleSort(arr):
  for i in range(len(arr)-1):
    flag = 0
    for j in range(len(arr)-i-1):
      if arr[j] > arr[j+1]:
        arr[j], arr[j+1] = arr[j+1], arr[j]
        flag = 1

    if flag == 0:
      break
  return arr

a = bubbleSort(read1)
for i in a:
  out.write('{} '.format(i))

out.close()

